interface JBIconProps {
  size: number
  class?: string
}

interface LinearGrad {
  x1: string
  x2: string
  y1: string
  y2: string
  stops: Array<{ offset: string, color: string }>
}

interface RadialGrad {
  transform: string
  stops: Array<{ offset: string, color: string }>
}

interface JBPath {
  fill: string
  d: string
}

interface JBSpec {
  // Outer marks (3 colored triangles, or 1 radial-gradient triangle for Idea CE / Rider).
  outer: JBPath[]
  // <defs> body — gradients keyed by id used in `outer.fill = url(#id)`.
  linears?: Record<string, LinearGrad>
  radials?: Record<string, RadialGrad>
  // The white IDE letterforms inside the inner dark square.
  letters: string[]
  // Sometimes the underbar moves (e.g. CLion uses width 16). Default 16.
  underbarD?: string
}

const JB_INNER_SQUARE_D = 'M52 12H12v40h40V12Z'
const JB_DEFAULT_UNDERBAR_D = 'M33 44H17v3h16v-3Z'

function JBIcon(props: JBIconProps & { spec: JBSpec }): JSX.Element {
  const idPrefix = createUniqueId()
  const grad = (key: string) => `${idPrefix}-${key}`
  const resolveFill = (fill: string) =>
    fill.startsWith('url(#') ? `url(#${grad(fill.slice(5, -1))})` : fill
  const linearEntries = () => Object.entries(props.spec.linears ?? {})
  const radialEntries = () => Object.entries(props.spec.radials ?? {})
  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={props.size}
      height={props.size}
      viewBox="0 0 64 64"
      fill="none"
      class={props.class}
      style={iconStyle(props.size)}
    >
      <defs>
        <For each={linearEntries()}>
          {([key, g]) => (
            <linearGradient id={grad(key)} x1={g.x1} x2={g.x2} y1={g.y1} y2={g.y2} gradientUnits="userSpaceOnUse">
              <For each={g.stops}>
                {s => <stop offset={s.offset} stop-color={s.color} />}
              </For>
            </linearGradient>
          )}
        </For>
        <For each={radialEntries()}>
          {([key, g]) => (
            <radialGradient id={grad(key)} cx="0" cy="0" r="1" gradientUnits="userSpaceOnUse" gradientTransform={g.transform}>
              <For each={g.stops}>
                {s => <stop offset={s.offset} stop-color={s.color} />}
              </For>
            </radialGradient>
          )}
        </For>
      </defs>
      <For each={props.spec.outer}>
        {p => <path fill={resolveFill(p.fill)} d={p.d} />}
      </For>
      <path fill="#000" d={JB_INNER_SQUARE_D} />
      <path fill="#fff" d={props.spec.underbarD ?? JB_DEFAULT_UNDERBAR_D} />
      <For each={props.spec.letters}>
        {d => <path fill="#fff" d={d} />}
      </For>
    </svg>
  )
}

