/** Generate a 4-word fingerprint from raw public key bytes. */
export function keyFingerprint(publicKey: Uint8Array): string {
  const hash = blake2b(publicKey, { dkLen: 32 })
  const words: string[] = []
  for (let i = 0; i < 4; i++) {
    words.push(WORDLIST[hash[i]])
  }
  return words.join('-')
}

/** Generate a 4-word fingerprint from a hex-encoded public key. */
export function keyFingerprintHex(publicKeyHex: string): string {
  const bytes = new Uint8Array(publicKeyHex.length / 2)
  for (let i = 0; i < bytes.length; i++) {
    bytes[i] = Number.parseInt(publicKeyHex.substring(i * 2, i * 2 + 2), 16)
  }
  return keyFingerprint(bytes)
}
